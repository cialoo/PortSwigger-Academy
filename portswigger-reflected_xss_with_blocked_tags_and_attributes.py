def discover_tags(tags):
       for tag in tags:
        payload = f"<{tag}>"
        
        try:
            request = requests.get(url, params={"search":payload})
            if request.status_code == 200:
                print(tag)
                print("-")
        except requests.RequestException as e:
            print(e)

def discover_attributes(attributes):
       for attribute in attributes:
        payload = f"<body {attribute}=1>" # body was identified as an allowed tag during tag enumeration
        
        try:
            request = requests.get(url, params={"search":payload})
            if request.status_code == 200:
                print(attribute)
                print("-")
        except requests.RequestException as e:
            print(e)

